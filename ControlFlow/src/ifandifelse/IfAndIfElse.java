package ifandifelse;

public class IfAndIfElse {
	public static void main(String[] args) {
	char n = 'A';

		switch (n) {
		case 65:
			System.out.println(n);
		case 2:
			System.out.println(n);
			break ;
		case 3:
			System.out.println(n);
			default :
				System.out.println("hello");
		
				
		}

	}
}				